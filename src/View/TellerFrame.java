    private void update_table() {
        try {
            ResultSet result = connect.startConnectionStatement().executeQuery("select * from client");
            client_table.setModel(DbUtils.resultSetToTableModel(result));

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        } finally {
            connect.closeConnection();
        }
    }

    private void update_table2(JTable table) {
        try {
            ResultSet result;
            if (table == transfer_table) {
                result = connect.startConnectionStatement().executeQuery("select * from client where c_national_id='" + static_nationalid + "' or c_national_id='" + reciver_national_id.getText() + "'");
            } else {
                result = connect.startConnectionStatement().executeQuery("select * from client where c_national_id='" + static_nationalid + "'");
            }

            table.setModel(DbUtils.resultSetToTableModel(result));
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        } finally {
            connect.closeConnection();
        }
    }

