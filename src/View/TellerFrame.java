    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (nationalId.getText().equals("") && pass.getText().equals("") && name.getText().equals("")
                && address.getText().equals("") && age.getText().equals("") && phone.getText().equals("")
                && money.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please, Fill All Fields");
        } else if (nationalId.getText().equals("") || pass.getText().equals("") || name.getText().equals("")
                || address.getText().equals("") || age.getText().equals("") || phone.getText().equals("")
                || money.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Complete All Data");
        } else if (Double.parseDouble(money.getText()) < 500) {
            JOptionPane.showMessageDialog(null, "You Must Deposit 500 LE Or More");
        } else if (nationalId.getText().length() < 14) {
            JOptionPane.showMessageDialog(null, "National ID Must Be 14 Digit");
        } else if (pass.getText().length() < 8) {
            JOptionPane.showMessageDialog(null, "Password Must Be 8 Digit Or More");
        } else if (phone.getText().length() < 11) {
            JOptionPane.showMessageDialog(null, "Phone Number Must Be 11 Digit");
        } else if (Integer.parseInt(age.getText()) < 21 || Integer.parseInt(age.getText()) > 120) {
            JOptionPane.showMessageDialog(null, "Your Age Must Between 21 and 120 Year");
        } else {
            new_client nnew = new new_client(nationalId.getText(), name.getText(), address.getText(), phone.getText(), Integer.parseInt(age.getText()));
            nnew.setMoney(Double.parseDouble(money.getText()));
            nnew.setPassword(pass.getText());

            teller t1 = new teller();
            t1.openNewaccount(nnew);
            if (teller.new_flag) {
                note.setVisible(false);
                update_table();
                clear();
            }
            else
                JOptionPane.showMessageDialog(null, "NO ROW CREATED");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

