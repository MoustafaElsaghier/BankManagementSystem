    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String a = nationalIDInput.getText();
        String p = passwordInput.getText();
        if (a.equals("") || p.equals("")) {
            JOptionPane.showMessageDialog(null, "Please Comlete Data");
        } else {
            try {
                ResultSet result = connect.startConnectionStatement().executeQuery("select * from client where c_national_id='" + nationalIDInput.getText() + "'");
                a = "";
                p = "";
                while (result.next()) {
                    a = result.getString(1);
                    p = result.getString(6);
                }
                if (a.equals(nationalIDInput.getText()) && p.equals(passwordInput.getText())) {
                    static_nationalid = nationalIDInput.getText();
                    cardPanel.removeAll();
                    cardPanel.add(oldClient);
                    cardPanel.repaint();
                    cardPanel.revalidate();
                    nationalIDInput.setText("");
                    passwordInput.setText("");
                    update_table2(depoist_table);
                    update_table2(withdraw_table);
                    update_table2(transfer_table);
                } else {
                    JOptionPane.showMessageDialog(null, "you entered wrong name or password");
                }

            } catch (Exception ex) {
                ex.printStackTrace();
            } finally {
                connect.closeConnection();
            }
        }

    }//GEN-LAST:event_jButton2ActionPerformed

